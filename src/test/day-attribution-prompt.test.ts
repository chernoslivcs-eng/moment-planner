// Крок 7 · фікс F1/N1/N2 — Парсинг: промпт Haiku має вчити ПРИНАЛЕЖНІСТЬ відносного дня.
// LLM у юніт-тесті не запускаємо — тестуємо КОНТРАКТ промпту: що відносний день ("завтра",
// "у понеділок") належить ЛИШЕ тому наміру, в якому прозвучав, і НЕ переноситься на сусідні
// наміри переліку; що daypart без явного дня = сьогодні; і що день+daypart разом дають І дату,
// І daypart. Плюс канонічний few-shot на конструкції DEMO1 (завтра A / по дорозі B / сьогодні C).

import { describe, expect, it } from "vitest";
import { SYSTEM_PROMPT } from "@/lib/parse/prompt";

describe("SYSTEM_PROMPT — приналежність відносного дня (контракт DEMO1)", () => {
  it("документує, що відносний день належить ТІЛЬКИ своєму наміру", () => {
    expect(SYSTEM_PROMPT).toContain("ПРИНАЛЕЖНІСТЬ ДНЯ");
    expect(SYSTEM_PROMPT).toContain("належить ТІЛЬКИ тому наміру");
  });

  it("забороняє перенесення дня на сусідні наміри переліку", () => {
    expect(SYSTEM_PROMPT).toContain("Не переноси його на сусідні наміри");
  });

  it("фіксує daypart без явного дня = сьогодні, і день+daypart разом = дата + daypart", () => {
    expect(SYSTEM_PROMPT).toContain("daypart без явного дня в цьому ж намірі — це СЬОГОДНІ");
    expect(SYSTEM_PROMPT).toContain('намір отримує І дату, І daypart');
  });

  it("несе канонічний few-shot конструкції DEMO1 (завтра A / по дорозі B / сьогодні C)", () => {
    expect(SYSTEM_PROMPT).toContain("Завтра зранку A, B по дорозі, і терміново сьогодні ввечері C");
    // B (по дорозі) — none, день НЕ переноситься
    expect(SYSTEM_PROMPT).toContain("завтра НЕ переноситься на B");
  });
});
