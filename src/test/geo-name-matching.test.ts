// Хід 1 гео (fix/geo-name-matching): reverse-геокодинг має віддавати ПОСЕЛЕННЯ, не адмінодиницю,
// а sameCity — толерантно зводити адмінформу («Славутицька міська громада») до її поселення
// («Славутич») за ТОЧНОЮ рівністю стемів із таблицею мутацій приголосних (ч→ц, к→ц, г→з), НЕ
// префіксом. Іменник↔іменник лишається точною рівністю, тож схожі топоніми (Славутич/Славута)
// не злипаються. Це презентаційно-збігова ланка гео — buildToday/контекст/статуси не чіпаються.

import { describe, expect, it } from "vitest";
import { sameCity } from "@/lib/conditions/location";
import { settlementFromAddress } from "@/lib/geo/currentCity";

describe("sameCity — іменник↔іменник (точна рівність)", () => {
  it("Київ = київ (регістр і пробіли)", () => {
    expect(sameCity("Київ", "  київ ")).toBe(true);
  });

  it("Львів = Львів", () => {
    expect(sameCity("Львів", "Львів")).toBe(true);
  });

  it("Київ ≠ Львів", () => {
    expect(sameCity("Київ", "Львів")).toBe(false);
  });

  it("«м.»/«місто» префікс не заважає (Львів)", () => {
    expect(sameCity("м. Львів", "Львів")).toBe(true);
    expect(sameCity("місто Київ", "київ")).toBe(true);
  });

  it("схожі топоніми лишаються різними: Славутич ≠ Славута", () => {
    expect(sameCity("Славутич", "Славута")).toBe(false);
    expect(sameCity("Славута", "Славутич")).toBe(false);
  });
});

describe("sameCity — адмінформа ↔ поселення (рівність стемів + мутація)", () => {
  it("«Славутицька міська громада» = «Славутич» (ч→ц), симетрично", () => {
    expect(sameCity("Славутицька міська громада", "Славутич")).toBe(true);
    expect(sameCity("Славутич", "Славутицька міська громада")).toBe(true);
  });

  it("«Львівська міська громада» = «Львів»", () => {
    expect(sameCity("Львівська міська громада", "Львів")).toBe(true);
  });

  it("«Львівський район» = «Львів»", () => {
    expect(sameCity("Львівський район", "Львів")).toBe(true);
  });

  it("«Львівська область» = «Львів»", () => {
    expect(sameCity("Львівська область", "Львів")).toBe(true);
  });

  it("«Кременчуцький район» = «Кременчук» (к→ц)", () => {
    expect(sameCity("Кременчуцький район", "Кременчук")).toBe(true);
  });

  it("мутація г→з у стемі (люксембурзький ↔ Люксембург)", () => {
    expect(sameCity("Люксембурзький район", "Люксембург")).toBe(true);
  });

  // НЕГАТИВНІ мандатні: різні поселення з різними базами не повинні злипатись через адмінформу.
  it("«Славутська міська громада» ≠ «Славутич» (інша база)", () => {
    expect(sameCity("Славутська міська громада", "Славутич")).toBe(false);
    expect(sameCity("Славутич", "Славутська міська громада")).toBe(false);
  });

  it("«Славутицька міська громада» ≠ «Славута»", () => {
    expect(sameCity("Славутицька міська громада", "Славута")).toBe(false);
    expect(sameCity("Славута", "Славутицька міська громада")).toBe(false);
  });

  it("різні міста в адмінформах не збігаються (Львівський ≠ Київський)", () => {
    expect(sameCity("Львівський район", "Київ")).toBe(false);
  });
});

// Хід 1 гео — Блок B: розширені мандатні пари компаратора (без мережі).
describe("sameCity — Блок B: мусять збігатись", () => {
  it("«Гадяцька міська громада» = «Гадяч» (-цька від кореня на -ч, не тупа симетрія)", () => {
    expect(sameCity("Гадяцька міська громада", "Гадяч")).toBe(true);
    expect(sameCity("Гадяч", "Гадяцька міська громада")).toBe(true);
  });

  it("«Ворзельська селищна громада» = «Ворзель» (селищна громада)", () => {
    expect(sameCity("Ворзельська селищна громада", "Ворзель")).toBe(true);
    expect(sameCity("Ворзель", "Ворзельська селищна громада")).toBe(true);
  });

  it("«м. Кам'янець-Подільський» = «Кам'янець-Подільський» (префікс + дефіс + апостроф)", () => {
    expect(sameCity("м. Кам'янець-Подільський", "Кам'янець-Подільський")).toBe(true);
  });
});

describe("sameCity — Блок B: НЕ мусять збігатись", () => {
  it("«Миколаїв» ≠ «Миколаївка»", () => {
    expect(sameCity("Миколаїв", "Миколаївка")).toBe(false);
  });

  it("«Городок» ≠ «Городня»", () => {
    expect(sameCity("Городок", "Городня")).toBe(false);
  });

  it("«Первомайськ» ≠ «Первомайське»", () => {
    expect(sameCity("Первомайськ", "Первомайське")).toBe(false);
  });

  it("«Ірпінь» ≠ «Київ» (супутник ≠ мегаполіс)", () => {
    expect(sameCity("Ірпінь", "Київ")).toBe(false);
  });
});

// ВІДОМА МЕЖА (не баг): чергування голосних у корені (Кривий Ріг → Криворізький) свідомо НЕ
// покривається — форвардна мутація «кривий ріг»→«кривий різьк» ≠ adjStem «криворізьк». Тест
// фіксує поточну (незбіжну) поведінку СЛОВАМИ, щоб зміна впала помітно, а не тихо.
describe("sameCity — Блок B: відома межа (документуємо, не фіксимо кодом)", () => {
  it("«Криворізький район» ≠ «Кривий Ріг» — vowel-alternation boundary", () => {
    expect(sameCity("Криворізький район", "Кривий Ріг")).toBe(false);
  });
});

describe("settlementFromAddress — поселення понад адмінодиницю", () => {
  it("бере city, коли воно є (Київ понад громаду)", () => {
    expect(
      settlementFromAddress({ city: "Київ", municipality: "Київська міська громада" }),
    ).toBe("Київ");
  });

  it("бере town понад municipality (Славутич понад громаду)", () => {
    expect(
      settlementFromAddress({ town: "Славутич", municipality: "Славутицька міська громада" }),
    ).toBe("Славутич");
  });

  it("бере village понад municipality", () => {
    expect(
      settlementFromAddress({ village: "Ромашки", municipality: "Ромашківська громада" }),
    ).toBe("Ромашки");
  });

  it("падає до municipality лише коли поселення нема зовсім", () => {
    expect(settlementFromAddress({ municipality: "Славутицька міська громада" })).toBe(
      "Славутицька міська громада",
    );
  });

  it("порожня адреса → null", () => {
    expect(settlementFromAddress({})).toBe(null);
  });

  it("порожні/пробільні значення ігноруються", () => {
    expect(settlementFromAddress({ city: "   ", town: "Славутич" })).toBe("Славутич");
  });
});
